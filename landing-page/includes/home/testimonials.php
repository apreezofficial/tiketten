<script>
document.addEventListener('DOMContentLoaded', function() {
    const carousel = document.querySelector('.testimonial-carousel');
    const track = carousel.querySelector('.flex');
    const slides = Array.from(document.querySelectorAll('.testimonial-slide'));
    const prevBtn = document.querySelector('.testimonial-prev');
    const nextBtn = document.querySelector('.testimonial-next');
    
    // Clone slides for infinite effect
    const clonedSlides = slides.map(slide => slide.cloneNode(true));
    clonedSlides.forEach(slide => track.appendChild(slide));
    
    const slideCount = slides.length;
    const slideWidth = slides[0].offsetWidth;
    let currentIndex = 0;
    let autoSlideInterval;
    
    // Update carousel position
    function updateCarousel() {
        track.style.transform = `translateX(-${currentIndex * slideWidth}px)`;
        
        // Reset position when reaching cloned slides for seamless infinite effect
        if (currentIndex >= slideCount) {
            setTimeout(() => {
                track.style.transition = 'none';
                currentIndex = 0;
                track.style.transform = `translateX(-${currentIndex * slideWidth}px)`;
                setTimeout(() => track.style.transition = 'transform 500ms ease-in-out');
            }, 500);
        } else if (currentIndex < 0) {
            setTimeout(() => {
                track.style.transition = 'none';
                currentIndex = slideCount - 1;
                track.style.transform = `translateX(-${currentIndex * slideWidth}px)`;
                setTimeout(() => track.style.transition = 'transform 500ms ease-in-out');
            }, 500);
        }
    }
    
    // Next slide
    function nextSlide() {
        currentIndex++;
        updateCarousel();
    }
    
    // Previous slide
    function prevSlide() {
        currentIndex--;
        updateCarousel();
    }
    
    // Auto slide
    function startAutoSlide() {
        autoSlideInterval = setInterval(nextSlide, 6000);
    }
    
    // Event listeners
    nextBtn.addEventListener('click', () => {
        clearInterval(autoSlideInterval);
        nextSlide();
        startAutoSlide();
    });
    
    prevBtn.addEventListener('click', () => {
        clearInterval(autoSlideInterval);
        prevSlide();
        startAutoSlide();
    });
    
    // Handle window resize
    let resizeTimer;
    window.addEventListener('resize', () => {
        clearTimeout(resizeTimer);
        resizeTimer = setTimeout(() => {
            slideWidth = slides[0].offsetWidth;
            updateCarousel();
        }, 250);
    });
  
    // Initialize
    updateCarousel();
    startAutoSlide();
    
    // Pause on hover
    carousel.addEventListener('mouseenter', () => clearInterval(autoSlideInterval));
    carousel.addEventListener('mouseleave', startAutoSlide);
});
</script>
