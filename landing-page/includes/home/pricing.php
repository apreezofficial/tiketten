<script>
document.addEventListener('DOMContentLoaded', function() {
    const toggle = document.getElementById('billing-toggle');
    const priceElements = document.querySelectorAll('.price-amount');
    const periodElements = document.querySelectorAll('.price-period');
    const annualPriceElements = document.querySelectorAll('.annual-price');
    
    // Monthly prices
    const monthlyPrices = ['$0', '$9', '$19'];
    const monthlyPeriods = ['/forever', '/month', '/month'];
    
    // Annual prices (20% discount)
    const annualPrices = ['$0', '$7', '$15'];
    const annualPeriods = ['/forever', '/month', '/month'];
    
    toggle.addEventListener('change', function() {
        if (this.checked) {
            // Switch to annual pricing
            priceElements.forEach((el, index) => {
                el.textContent = annualPrices[index];
            });
            periodElements.forEach((el, index) => {
                el.textContent = annualPeriods[index];
            });
            annualPriceElements.forEach(el => {
                el.classList.remove('hidden');
            });
        } else {
            // Switch to monthly pricing
            priceElements.forEach((el, index) => {
                el.textContent = monthlyPrices[index];
            });
            periodElements.forEach((el, index) => {
                el.textContent = monthlyPeriods[index];
            });
            annualPriceElements.forEach(el => {
                el.classList.add('hidden');
            });
        }
    });
});
</script>